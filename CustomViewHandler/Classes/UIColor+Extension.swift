import UIKit

extension UIColor
{
    public static var skyBlueColor: UIColor
    {
        return UIColor(red: 135/255, green: 206/255, blue: 235/255, alpha: 1.0)
    }
    
    public static var maroonRedColor: UIColor
    {
        return UIColor(red: 128/255, green: 0/255, blue: 0/255, alpha: 1.0)
    }
    
    public static var lemonYellowColor: UIColor
    {
        return UIColor(red: 255/255, green: 247/255, blue: 0/255, alpha: 1.0)
    }
    
    public static var strawberryPinkColor: UIColor
    {
        return UIColor(red: 252/255, green: 90/255, blue: 141/255, alpha: 1.0)
    }
    
    public static var neonGreenColor: UIColor
    {
        return UIColor(red: 57/255, green: 255/255, blue: 20/255, alpha: 1.0)
    }
}
